class Operators{
    public static void main(String[] args){
        int a=45;
        int b=56;
        int sum=a+b;
        int diff=a-b;
        int product=a*b;
        int division=a%b;
        int floor=a/b;
        System.out.println(sum);
        System.out.println(diff);
        System.out.println(product);
        System.out.println(division);
        System.out.println(floor);
    }
}